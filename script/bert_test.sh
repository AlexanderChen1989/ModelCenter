#! /bin/bash

DLS_TASK_NUMBER=1

if [[ $DLS_TASK_NUMBER == 1 ]]; then
    MASTER_ADDR=localhost
    MASTER_PORT=6000
    NNODES=1
    NODE_RANK=0
else
    MASTER_HOST="$BATCH_CUSTOM0_HOSTS"
    MASTER_ADDR="${MASTER_HOST%%:*}"
    MASTER_PORT="${MASTER_HOST##*:}"
    NNODES="$DLS_TASK_NUMBER"
    NODE_RANK="$DLS_TASK_INDEX"
fi

GPUS_PER_NODE=1

DISTRIBUTED_ARGS="--nproc_per_node $GPUS_PER_NODE \
                  --nnodes $NNODES \
                  --node_rank $NODE_RANK \
                  --master_addr $MASTER_ADDR \
                  --master_port $MASTER_PORT"

BASEDIR="/home/hx/lyq/BigModels/src/"
OPTS=""
OPTS+=" --world-size 4"

CMD="python3 -m torch.distributed.launch ${DISTRIBUTED_ARGS} ${BASEDIR}_bert_test.py ${OPTS}"
echo ${CMD}

${CMD}
